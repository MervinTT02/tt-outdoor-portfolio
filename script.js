const allPhotos = routes.flatMap((route) =>
  route.photos.map((src, index) => ({
    routeId: route.id,
    routeName: route.name,
    src,
    index: index + 1,
    fileName: getFileName(src),
  })),
);

let activeFilter = "all";
let visiblePhotos = allPhotos;
let lightboxIndex = 0;

function assetPath(path) {
  return encodeURI(path).replace(/#/g, "%23");
}

function getFileName(path) {
  return path.split("/").pop() || "";
}

function shortName(fileName) {
  return fileName.replace(/\.[^/.]+$/, "");
}

function setStats() {
  document.getElementById("stat-photo-count").textContent = String(
    allPhotos.length,
  );
  document.getElementById("stat-route-count").textContent = String(routes.length);
  document.getElementById("stat-updated").textContent = "v1.0";
  document.getElementById("footer-year").textContent = String(
    new Date().getFullYear(),
  );
}

function setHeroCover() {
  const hero = document.getElementById("hero-cover");
  const picks = routes
    .map((route) => route.photos[Math.floor(Math.random() * route.photos.length)])
    .filter(Boolean);
  const selected = picks[Math.floor(Math.random() * picks.length)];
  if (selected) {
    hero.src = assetPath(selected);
  }
}

function renderRoutes() {
  const routeGrid = document.getElementById("route-grid");
  routeGrid.innerHTML = "";

  routes.forEach((route) => {
    const card = document.createElement("article");
    card.className = "route-card";
    const cover = route.photos[Math.floor(route.photos.length / 2)] || route.photos[0];
    card.innerHTML = `
      <img src="${assetPath(cover)}" alt="${route.name} 封面" loading="lazy" />
      <div class="route-body">
        <h3 class="route-name">${route.name}</h3>
        <p class="route-meta">${route.location} · ${route.period}</p>
        <p class="route-meta">${route.effort}</p>
        <p class="route-highlight">${route.highlight}</p>
        <button class="route-action" data-route-id="${route.id}">查看该路线作品（${route.photos.length}）</button>
      </div>
    `;
    routeGrid.appendChild(card);
  });

  routeGrid.querySelectorAll(".route-action").forEach((button) => {
    button.addEventListener("click", () => {
      const routeId = button.getAttribute("data-route-id");
      if (!routeId) return;
      setFilter(routeId);
      document.getElementById("gallery").scrollIntoView({
        behavior: "smooth",
        block: "start",
      });
    });
  });
}

function renderFilters() {
  const filterRow = document.getElementById("filter-row");
  filterRow.innerHTML = "";

  const allBtn = document.createElement("button");
  allBtn.className = `filter-btn ${activeFilter === "all" ? "active" : ""}`;
  allBtn.textContent = `全部 (${allPhotos.length})`;
  allBtn.addEventListener("click", () => setFilter("all"));
  filterRow.appendChild(allBtn);

  routes.forEach((route) => {
    const button = document.createElement("button");
    button.className = `filter-btn ${activeFilter === route.id ? "active" : ""}`;
    button.textContent = `${route.name} (${route.photos.length})`;
    button.addEventListener("click", () => setFilter(route.id));
    filterRow.appendChild(button);
  });
}

function setFilter(routeId) {
  activeFilter = routeId;
  visiblePhotos =
    routeId === "all"
      ? allPhotos
      : allPhotos.filter((photo) => photo.routeId === routeId);
  renderFilters();
  renderGallery();
}

function renderGallery() {
  const gallery = document.getElementById("gallery-grid");
  gallery.innerHTML = "";

  visiblePhotos.forEach((photo, index) => {
    const card = document.createElement("article");
    card.className = "photo-card";
    card.innerHTML = `
      <img src="${assetPath(photo.src)}" alt="${photo.routeName} 第 ${photo.index} 张" loading="lazy" decoding="async" />
      <div class="photo-caption">
        <p>${photo.routeName} · ${shortName(photo.fileName)}</p>
      </div>
    `;
    card.addEventListener("click", () => openLightbox(index));
    gallery.appendChild(card);
  });
}

function openLightbox(index) {
  lightboxIndex = index;
  const lightbox = document.getElementById("lightbox");
  lightbox.classList.add("open");
  lightbox.setAttribute("aria-hidden", "false");
  document.body.style.overflow = "hidden";
  renderLightbox();
}

function closeLightbox() {
  const lightbox = document.getElementById("lightbox");
  lightbox.classList.remove("open");
  lightbox.setAttribute("aria-hidden", "true");
  document.body.style.overflow = "";
}

function renderLightbox() {
  const current = visiblePhotos[lightboxIndex];
  if (!current) return;
  const image = document.getElementById("lightbox-image");
  const caption = document.getElementById("lightbox-caption");
  image.src = assetPath(current.src);
  image.alt = `${current.routeName} 大图预览`;
  caption.textContent = `${current.routeName} · ${shortName(current.fileName)} · ${lightboxIndex + 1} / ${visiblePhotos.length}`;
}

function showPrev() {
  lightboxIndex =
    (lightboxIndex - 1 + visiblePhotos.length) % visiblePhotos.length;
  renderLightbox();
}

function showNext() {
  lightboxIndex = (lightboxIndex + 1) % visiblePhotos.length;
  renderLightbox();
}

function bindLightboxEvents() {
  document
    .getElementById("lightbox-close")
    .addEventListener("click", closeLightbox);
  document.getElementById("lightbox-prev").addEventListener("click", showPrev);
  document.getElementById("lightbox-next").addEventListener("click", showNext);
  document.getElementById("lightbox").addEventListener("click", (event) => {
    if (event.target.id === "lightbox") closeLightbox();
  });
  document.addEventListener("keydown", (event) => {
    const lightbox = document.getElementById("lightbox");
    if (!lightbox.classList.contains("open")) return;
    if (event.key === "Escape") closeLightbox();
    if (event.key === "ArrowLeft") showPrev();
    if (event.key === "ArrowRight") showNext();
  });
}

function initReveal() {
  const targets = document.querySelectorAll(".reveal");
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.15 },
  );
  targets.forEach((target) => observer.observe(target));
}

function init() {
  setStats();
  setHeroCover();
  renderRoutes();
  renderFilters();
  renderGallery();
  bindLightboxEvents();
  initReveal();
}

document.addEventListener("DOMContentLoaded", init);
